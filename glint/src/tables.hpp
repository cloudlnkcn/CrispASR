// ---------------------------------------------------------------------------
// Runtime initialization
// ---------------------------------------------------------------------------

inline bool tables_initialized = false;

inline void init_tables() {
    if (tables_initialized) return;

    // ----- Analysis window (512 coefficients) -----
    // Convert the exact ISO 11172-3 Table C.1 coefficients to Q31 fixed-point.
    constexpr double PI = 3.14159265358979323846;

    // Store as Q30 (range [-2.0, +2.0)) since some coefficients exceed 1.0
    // Q30: multiply by 2^30 = 1073741824
    for (int i = 0; i < 512; ++i) {
        double scaled = analysis_window_d[i] * 1073741824.0;
        analysis_window[i] = static_cast<int32_t>(
            scaled + (scaled >= 0 ? 0.5 : -0.5)
        );
    }

    // ----- MDCT cosine matrix (36x18) -----
    // cos(pi/72 * (2*n + 19) * (2*k + 1))
    for (int n = 0; n < 36; ++n) {
        for (int k = 0; k < 18; ++k) {
            double angle = PI / 72.0 * (2.0 * n + 19.0) * (2.0 * k + 1.0);
            double val = std::cos(angle);
            // Convert to Q31 with clamping
            double scaled = val * 2147483648.0;
            double rounded = scaled + (scaled >= 0 ? 0.5 : -0.5);
            if (rounded > 2147483647.0) rounded = 2147483647.0;
            if (rounded < -2147483648.0) rounded = -2147483648.0;
            mdct_cos[n][k] = static_cast<int32_t>(rounded);
        }
    }

    // ----- MDCT window for long blocks -----
    // sin(pi/36 * (n + 0.5)) for n = 0..35
    for (int n = 0; n < 36; ++n) {
        double val = std::sin(PI / 36.0 * (n + 0.5));
        double scaled = val * 2147483648.0;
        mdct_window[n] = static_cast<int32_t>(
            scaled + (scaled >= 0 ? 0.5 : -0.5)
        );
    }

    // ----- Aliasing reduction coefficients (ISO 11172-3 Table B.9) -----
    {
        static constexpr double c[8] = {
            -0.6, -0.535, -0.33, -0.185, -0.095, -0.041, -0.0142, -0.0037
        };
        for (int i = 0; i < 8; ++i) {
            double denom = std::sqrt(1.0 + c[i] * c[i]);
            double cs_val = 1.0 / denom;
            double ca_val = c[i] / denom;
            double cs_scaled = cs_val * 2147483648.0;
            double ca_scaled = ca_val * 2147483648.0;
            alias_cs[i] = static_cast<int32_t>(
                cs_scaled + (cs_scaled >= 0 ? 0.5 : -0.5));
            alias_ca[i] = static_cast<int32_t>(
                ca_scaled + (ca_scaled >= 0 ? 0.5 : -0.5));
        }
    }

    // ----- x^(3/4) lookup table (double precision) -----
    pow34_table[0] = 0.0;
    for (int x = 1; x < kPow34TableSize; ++x) {
        pow34_table[x] = std::pow(static_cast<double>(x), 0.75);
    }

    // ----- Subband matrixing table (32x64) -----
    // M[i][k] = cos((2*i + 1) * (k - 16) * pi / 64)
    for (int i = 0; i < 32; ++i) {
        for (int k = 0; k < 64; ++k) {
            double angle = (2.0 * i + 1.0) * (k - 16.0) * PI / 64.0;
            double val = std::cos(angle);
            double scaled = val * 2147483648.0;
            // Clamp to Q31 range: cos(0) = 1.0 overflows Q31 (max representable
            // value is (2^31-1)/2^31 ≈ 0.9999999995). Clamp to INT32_MAX.
            double rounded = scaled + (scaled >= 0 ? 0.5 : -0.5);
            if (rounded > 2147483647.0) rounded = 2147483647.0;
            if (rounded < -2147483648.0) rounded = -2147483648.0;
            subband_matrix[i][k] = static_cast<int32_t>(rounded);
        }
    }

    // ----- Subband matrixing table Q15 -----
    for (int i = 0; i < 32; ++i) {
        for (int k = 0; k < 64; ++k) {
            double angle = (2.0 * i + 1.0) * (k - 16.0) * PI / 64.0;
            double val = std::cos(angle);
            subband_matrix_q15[i][k] = static_cast<int16_t>(
                val * 32767.0 + (val >= 0 ? 0.5 : -0.5)
            );
        }
    }

    // ----- Double-precision subband matrixing table -----
    for (int i = 0; i < 32; ++i) {
        for (int k = 0; k < 64; ++k) {
            subband_matrix_d[i][k] = std::cos((2.0 * i + 1.0) * (k - 16.0) * PI / 64.0);
        }
    }

#if !defined(GLINT_FIXED_POINT) || defined(GLINT_BOTH_PATHS)
    // ----- 2^(-0.25*n) table -----
    for (int n = 0; n < 64; ++n) {
        pow2_neg_quarter[n] = std::pow(2.0, -0.25 * n);
    }
#endif

    tables_initialized = true;
}

