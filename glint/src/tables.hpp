// ---------------------------------------------------------------------------

inline int huff_code_length(int table_id, int x, int y) {
    HuffTable ht = get_huff_table(table_id);
    int extra = 0;

    // Handle sign bits (1 bit per nonzero value)
    if (x != 0) extra++;
    if (y != 0) extra++;

    int ax = (x < 0) ? -x : x;
    int ay = (y < 0) ? -y : y;

    if (table_id >= 16 && table_id <= 31 && ht.linbits > 0) {
        // ESC table: values >= 15 use linbits (decoder always reads linbits when value==max)
        if (ax >= 15) {
            extra += ht.linbits;
            ax = 15;
        }
        if (ay >= 15) {
            extra += ht.linbits;
            ay = 15;
        }
    }

    if (ax >= ht.xlen || ay >= ht.xlen) {
        return 100; // invalid - very large penalty
    }

    return ht.hlen[ax * ht.xlen + ay] + extra;
}

// Utility: count1 code length for 4 values (v, w, x, y) each 0 or 1
inline int count1_code_length(int table_id, int v, int w, int x, int y) {
    // table_id should be 32 or 33
    int idx = ((v != 0) ? 8 : 0) | ((w != 0) ? 4 : 0) |
              ((x != 0) ? 2 : 0) | ((y != 0) ? 1 : 0);
    int sign_bits = (v != 0) + (w != 0) + (x != 0) + (y != 0);

    if (table_id == 32) {
        return ht32_len[idx] + sign_bits;
    } else {
        return ht33_len[idx] + sign_bits;
    }
}

// ---------------------------------------------------------------------------
// Choose best Huffman table for a region with given max absolute value
// Returns table_id, or 0 if no suitable table found
// ---------------------------------------------------------------------------

inline int choose_huff_table(int max_val) {
    if (max_val == 0) return 0;
    if (max_val <= 1) return 1;
    if (max_val <= 2) return 3;   // table 2 or 3
    if (max_val <= 3) return 5;   // table 5 or 6
    if (max_val <= 5) return 7;   // table 7, 8, or 9
    if (max_val <= 7) return 10;  // table 10, 11, or 12
    if (max_val <= 15) return 13; // table 13 or 15
    // For values > 15, need ESC tables (16-31)
    // Choose based on how many linbits needed
    int bits_needed = 0;
    int tmp = max_val - 15;
    while (tmp > 0) { bits_needed++; tmp >>= 1; }

    // Find a table with enough linbits
    for (int t = 16; t < 32; ++t) {
        if (kLinbits[t - 16] >= bits_needed) return t;
    }
    return 24; // fallback
}

